class Calculator:
    def setCal(self, first, second):
        self.first = first
        self.second = second

    def plus(self):
        return self.first + self.second

    def minus(self):
        return self.first - self.second

    def multi(self):
        return self.first * self.second

    def divide(self):
        return self.first / self.second


        