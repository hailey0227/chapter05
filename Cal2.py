class Cal2:
    def setdata(self, a, b):
        self.a = a
        self.b = b

    def plus(self):
        return self.a + self.b

    def minus(self):
        return self.a - self.b

    def multi(self):
        return self.a * self.b

    def divide(self):
        return self.a / self.b